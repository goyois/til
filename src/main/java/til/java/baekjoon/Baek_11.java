package til.java.baekjoon;

import java.util.Scanner;


public class Baek_11 {
    public static void main(String[] args) {

        Scanner sc  = new Scanner(System.in);

        while (sc.hasNext()) {
            int a = sc.nextInt();
            int b = sc.nextInt();
            System.out.println(a + b);
        }
        sc.close();
    }
}

